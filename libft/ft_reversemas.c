#include "libft.h"

char	*ft_reversemas(char *s)
{
	int		i;
	int		j;
	char	c;

	i = 0;
	j = strlen(s) - 1;
	if (s)
		while (i < j)
		{
			c = s[j];
			s[j--] = s[i];
			s[i++] = c;
		}
	return (s);
}
