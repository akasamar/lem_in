#include "libft.h"

int		ft_cntwithoutspaces(char *str)
{
	int i;

	i = 0;
	while (*str)
	{
		if (!(*str == '\t' || *str == '\n' || *str == ' '))
			i++;
		str++;
	}
	return (i);
}
